    private void setupSplashScreen() {
        // Center the window on screen
        setLocationRelativeTo(null);
        
        // Start the loading process
        startLoading();
    }
    private void startLoading() {
        SwingWorker<Void, Integer> worker = new SwingWorker<Void, Integer>() {
            @Override
            protected Void doInBackground() throws Exception {
                // Simulate loading tasks with progress updates
                String[] tasks = {
                    "Loading application resources...",
                    "Initializing database connection...", 
                    "Setting up user interface...",
                    "Loading configuration...",
                    "Starting application..."
                };
                
                for (int i = 0; i < tasks.length; i++) {
                    // Update status (we'll add a status label in GUI Builder)
                    publish((i + 1) * 20); // 20%, 40%, 60%, 80%, 100%
                    
                    // Simulate task processing time
                    Thread.sleep(800);
                }
                return null;
            }
             @Override
            protected void process(java.util.List<Integer> chunks) {
                // Update progress bar
                int progress = chunks.get(chunks.size() - 1);
                jProgressBar1.setValue(progress);
            }
            
            @Override
            protected void done() {
                try {
                    // Close splash screen and open main application
                    dispose();
                    
                    // Open the main registration form
                    java.awt.EventQueue.invokeLater(() -> {
                        RegistrationForm registrationForm = new RegistrationForm();
                        registrationForm.setVisible(true);
                        registrationForm.setLocationRelativeTo(null); // Center the main form
                    });
                } catch (Exception e) {
                    e.printStackTrace();
                    // If RegistrationForm doesn't exist yet, just close
                    System.exit(0);
                }
            }
        };
        
        worker.execute();
    }
